#- Algoritmo para calcular el área de un circulo
#    - Inicio
#    - Decirle al usuario que digite el radio del circulo
#    - Leer radio
#    - Hacer la formula para conseguir el área de un circulo ( π*r² )
#    - Mostrar resultado
#    - Fin

radio = int(input("DIgite el radio del circulo en cm: "))

Area = 3.1416 * (radio^2)

print(Area)